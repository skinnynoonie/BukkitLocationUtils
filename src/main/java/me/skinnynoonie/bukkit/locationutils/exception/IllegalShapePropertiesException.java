package me.skinnynoonie.bukkit.locationutils.exception;

public class IllegalShapePropertiesException extends RuntimeException {

    public IllegalShapePropertiesException() {
        super();
    }

    public IllegalShapePropertiesException(String message) {
        super(message);
    }

    public IllegalShapePropertiesException(String message, Throwable cause) {
        super(message, cause);
    }

    public IllegalShapePropertiesException(Throwable cause) {
        super(cause);
    }

}
